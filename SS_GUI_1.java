    private void select_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser();
        
              
          
         if(fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION)
         {
            java.io.File file = fileChooser.getSelectedFile() ;
            filepath_display.setText("");
            filepath_display.setText(file.getAbsolutePath());
            filename =  new String(file.getAbsolutePath()) ;
            if (!filename.endsWith(".txt") )
               {
                   JOptionPane.showMessageDialog(this, "not a valid file : please select .txt fromat");
                    return ;     
                }
            
            
         }
         else
         {  
             filename = "" ;
             JOptionPane.showMessageDialog(this, "file not selected");
             return ;    
         }
         
         
         //parse button setting
          parse_button.setFont(new java.awt.Font("Times New Roman", 1, 24)); // NOI18N
          parse_button.setForeground(new java.awt.Color(43, 195, 51));
          parse_button.setText("PARSE FILE");
          parse_button.setEnabled(true);
          parse_button.setVisible(true);
         
         // cancel button setting
          cancle_button.setFont(new java.awt.Font("Times New Roman", 1, 24)); // NOI18N
          cancle_button.setForeground(new java.awt.Color(247, 40, 56));
          cancle_button.setText("CANCLE");
          cancle_button.setEnabled(true);
          cancle_button.setVisible(true);
          
          
         select_button.setEnabled(false);
         select_button.setVisible(false);
         
    }                                             

