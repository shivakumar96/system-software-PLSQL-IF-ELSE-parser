    private void parse_buttonActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:
        int result ;  
        
         if( filename.equalsIgnoreCase("")){
               JOptionPane.showMessageDialog(this,"file not selected");
        }
        

        Result_display r ;
        
         result = cl.callParser(filename) ;

        if(result == -1 ){
               JOptionPane.showMessageDialog(this,"internal error occured");
        }else if( result == 0){
              r = new Result_display() ;
              r.printSuccess();
              r.setVisible(true);
    
        }else {
               
              r = new Result_display() ;
              r.printFailure(result);
              r.setVisible(true);

            }
        
      

    }                                            

